-- КЕЙС №13: РАСЧЕТ НАКОПИТЕЛЬНОГО ИТОГА (RUNNING TOTAL) ДЛЯ МОНИТОРИНГА КАССОВЫХ РАЗРЫВОВ
-- Бизнес-задача: Сформировать непрерывный хронологический реестр выручки с расчетом кумулятивной суммы (нарастающего итога) без агрегации и потери детальных строк транзакций.

SELECT
    sale_date AS sale_timestamp,           -- Точная временная метка совершения торговой операции
    COALESCE(revenue, 0) AS clean_revenue, -- Финальная валидация: нейтрализация NULL во второй колонке вывода
    
    -- СКОЛЬЗЯЩЕЕ ОКОННОЕ СУММИРОВАНИЕ: Отсутствие PARTITION BY разворачивает расчет на весь массив данных.
    -- Связка sale_date и sale_id в ORDER BY гарантирует уникальность каждой точки расчета.
    -- Это исключает баг группового скачка на идентичных временных метках и реализует 
    -- пошаговый нарастающий итог (ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW).
    SUM(COALESCE(revenue, 0)) OVER (
        ORDER BY sale_date, sale_id
    ) AS running_total_revenue

FROM shop_sales

-- СОРТИРОВКА ВИТРИНЫ: Глобальное упорядочивание реестра строго по возрастанию времени
ORDER BY sale_date ASC;
