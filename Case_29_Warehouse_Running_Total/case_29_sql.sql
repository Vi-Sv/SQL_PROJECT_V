-- Бизнес-задача: Спроектировать высокопроизводительный аналитический конвейер для расчета скользящего товарного баланса. Реализовать кумулятивное суммирование с динамической сменой знака операции (плюс для прихода, минус для возврата) без сжатия детальных логов складского учета.

SELECT 
    log_id AS "ID", 
    TRIM(LOWER(operator_name)) AS g1,
    
    -- СКОЛЬЗЯЩЕЕ УСЛОВНОЕ ОКOННОЕ СУММИРОВАНИЕ:
    -- [Исправление] Вшит утерянный токен CASE и выровнена скобочная структура перед оператором OVER.
    -- Функция CASE WHEN на лету переключает математический знак (+/-) в зависимости от типа операции.
    -- PARTITION BY изолирует расчетные контуры по каждому оператору склада.
    -- ORDER BY log_id фиксирует строгую хронологическую последовательность нарастания баланса.
    -- Рамка ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW задает непрерывный скользящий подсчет от первой строки до текущей точки.
    SUM(
        CASE 
            WHEN operation_type = 'ПРИХОД' THEN COALESCE(quantity, 0)
            WHEN operation_type = 'ВОЗВРАТ' THEN -COALESCE(quantity, 0)
            ELSE 0
        END
    ) OVER (
        PARTITION BY TRIM(LOWER(operator_name)) 
        ORDER BY log_id -- [Исправление] Дописана буква "d" в имя ключевого поля log_id
        ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
    ) AS "Накопительный_баланс_склада"

FROM warehouse_logs

-- СОРТИРОВКА ВИТРИНЫ: Регламентное упорядочивание по алфавиту операторов и номерам логов
ORDER BY 2, 1; -- [Исправление] Восстановлено корректное написание оператора ORDER BY
