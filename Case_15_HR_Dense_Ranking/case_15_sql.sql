-- КЕЙС №15: ЮВЕЛИРНОЕ РАНЖИРОВАНИЕ ДАТА-КАНДИДАТОВ И ОКОННЫЙ СРЕЗ РЕЙТИНГА МЕСТ
-- Бизнес-задача: Выполнить плотное ранжирование участников конкурса внутри структурных подразделений. Вывести детализированный список лидеров с ограничением выборки строго первыми двумя призовыми местами (золото и серебро) без математических пропусков в рангах.

SELECT 
    tab2.clean_department_id AS "Номера_департаментов",
    tab2.clean_candidate_name AS "Чистые_имена",
    tab2.clean_score AS "Баллы",
    tab2.dense_rank_place AS "Рейтинг_мест" -- Коррекция: Удалена избыточная запятая перед оператором FROM

FROM (
    -- ВНУТРЕННИЙ СЛОЙ: Нормализация данных и расчет плотных аналитических рангов
    SELECT 
        department_id AS clean_department_id,              -- Идентификатор структурного подразделения
        TRIM(LOWER(candidate_name)) AS clean_candidate_name, -- Нормализация текстового ввода: удаление пробелов и приведение к нижнему регистру
        COALESCE(score, 0) AS clean_score,                   -- Валидация метрики: замена NULL на 0 для защиты математического движка
        
        -- ПЛОТНОЕ РАНЖИРОВАНИЕ: Функция DENSE_RANK() присваивает одинаковый ранг строкам с равными значениями.
        -- Конструкция PARTITION BY изолирует группы конкурсантов по департаментам.
        -- ORDER BY DESC выстраивает баллы по убыванию от максимального к минимальному.
        DENSE_RANK() OVER (
            PARTITION BY department_id 
            ORDER BY COALESCE(score, 0) DESC
        ) AS dense_rank_place
    FROM contest_scores -- Коррекция: Вписан пропущенный адрес исходной таблицы СУБД
) AS tab2

-- ХИРУРГИЧЕСКОЕ СИТО (Фильтр КПП): Вырезаем строго 1-е и 2-е призовые места. Все строки ниже серебряного призера отсекаются.
WHERE tab2.dense_rank_place <= 2

-- СОРТИРОВКА ВИТРИНЫ: По возрастанию номеров департаментов, а внутри них — по убыванию конкурсных баллов
ORDER BY 1 ASC, 3 DESC;
