-- Бизнес-задача: Сформировать консолидированный реестр взаиморасчетов по всей структуре контрагентов холдинга. Обеспечить сквозную интеграцию трех таблиц с сохранением позиций партнеров без договоров или транзакционной активности.

SELECT 
    TRIM(LOWER(t1.agent_name)) AS agent_name,       -- Унификация текстовых данных: удаление пробелов и приведение к нижнему регистру
    TRIM(LOWER(t2.contract_type)) AS contract_type, -- Нормализация признаков типов соглашений
    COALESCE(t3.total_amount, 0) AS total_payments  -- Финальная валидация: замена внешних NULL на 0 для сохранения целостности отчета

FROM agents AS t1 -- Исходный справочник контрагентов (Базовая левая неприкасаемая таблица)

-- ПЕРВЫЙ МОСТ: Левостороннее объединение для сохранения агентов без активных контрактов
LEFT JOIN contracts AS t2 
    ON t1.agent_id = t2.agent_id

-- ВТОРОЙ МОСТ: Левостороннее сопряжение со сжатым подзапросом транзакций
LEFT JOIN (
    -- ПРЕДВАРИТЕЛЬНАЯ АГРЕГАЦИЯ: Схлопывание дублирующих строк платежей по договорам до этапа сшивки
    -- Изоляция суммирования блокирует риск логического вспучивания данных на верхнем уровне
    SELECT 
        contract_id, 
        SUM(COALESCE(amount, 0)) AS total_amount -- Внутренний предохранитель от пропусков данных (NULL) в логах платежей
    FROM payments
    GROUP BY contract_id
) AS t3
    ON t2.contract_id = t3.contract_id; -- Ключевое условие сопряжения по идентификатору договора
