-- Бизнес-задача: Устранить последствия технического сбоя весового терминала (дублирование накладных) и рассчитать чистый средний объем поставок по городам.

SELECT 
    NEW_TAB.CLEAN_CITY, 
    -- Вычисляем среднее значение объема по очищенным данным и округляем до сотых долей
    AVG(NEW_TAB.NOTNULLVOL)::NUMERIC(10,2) AS "Средний объем поставки"

FROM (
    -- ВНУТРЕННИЙ СЛОЙ («МАТРЕШКА»): Схлопывание полных дубликатов строк
    SELECT
        TRIM(LOWER(city)) AS CLEAN_CITY,          -- Унификация текстовых регистров и удаление концевых пробелов
        TRIM(LOWER(provider_name)) AS CLEAN_NAME, -- Нормализация наименований поставщиков
        ship_time,                                -- Точная временная метка транзакции
        COALESCE(volume, 0) AS NOTNULLVOL         -- Локализация пропусков (NULL) для корректной агрегации
    FROM raw_shipments
    GROUP BY 
        TRIM(LOWER(city)), 
        TRIM(LOWER(provider_name)), 
        ship_time, 
        COALESCE(volume, 0) -- Группировка по всем полям намертво ликвидирует идентичные строки-клоны
) AS NEW_TAB

GROUP BY NEW_TAB.CLEAN_CITY; -- Финальная агрегация очищенных показателей по географическим узлам

