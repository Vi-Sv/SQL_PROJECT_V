-- КЕЙС №3: АНАЛИЗ ТРАНЗАКЦИОННЫХ АНОМАЛИЙ И СРАВНИТЕЛЬНЫЙ АУДИТ ФИНТЕХ-ПОТОКА
-- Бизнес-задача: Сформировать детализированный реестр операций за отчетный период с расчетом отклонений от среднего значения без агрегации и потери детальных строк.

SELECT 
    tx_id AS transaction_id,  -- Уникальный идентификатор операции (Первичный ключ)
    user_id AS user_id,        -- Уникальный идентификатор клиента
    
    -- 1. ВАЛИДАЦИЯ: убираем пустые ячейки NULL и переводим отрицательные возвраты в чистый положительный объем
    ABS(COALESCE(amount, 0)) AS clean_amount,
    
    -- 2. ОКОННЫЙ КОНВЕЙЕР: Оконная форсунка OVER() без разделений вычисляет среднее по всей выборке за отчетный период,
    -- округляет результат до сотых долей через ::NUMERIC(10,2) и аккуратно клеится в четвертую колонку без склеек детального отчета
    AVG(ABS(COALESCE(amount, 0))) OVER()::NUMERIC(10,2) AS average_amount_period

FROM raw_transactions

-- 3. ЭЛЕГАНТНАЯ ФИЛЬТРАЦИЯ: Вырезаем целевой месяц и фиксируем отчетный год через инструменты EXTRACT
WHERE EXTRACT(MONTH FROM date) = 5 
  AND EXTRACT(YEAR FROM date) = 2026

-- 4. СОРТИРОВКА: Выводим топовые транзакции по убыванию очищенного объема наверх реестра
ORDER BY clean_amount DESC;
