-- Бизнес-задача: Сформировать детализированный график отгрузки строительных материалов с расчетом временной метки следующей хронологической поставки для каждого номенклатурного узла без потери детальных строк.

SELECT
    TRIM(LOWER(material_name)) AS clean_material_name, -- Нормализация данных: удаление пробелов и приведение к нижнему регистру для защиты от грязного ручного ввода
    delivery_date AS current_delivery_date,             -- Точная дата и время фактической отгрузки на объект
    tonnage AS current_tonnage,                         -- Объем текущей поставки в тоннах
    
    -- ОКОННОЕ СМЕЩЕНИЕ ВПЕРЕД: Функция LEAD заглядывает на 1 строку вперед по временной оси.
    -- PARTITION BY изолирует историю логистики каждого конкретного материала.
    -- ORDER BY ASC упорядочивает транзитный поток по возрастанию дат.
    -- Дефолтное значение опущено для сохранения консистентности типа данных TIMESTAMP.
    LEAD(delivery_date, 1) OVER (
        PARTITION BY TRIM(LOWER(material_name)) 
        ORDER BY delivery_date ASC
    ) AS next_delivery_date

FROM materials_delivery

-- СОРТИРОВКА ВИТРИНЫ: Регламентное выравнивание отчета по алфавиту материалов и хронологии рейсов
ORDER BY clean_material_name ASC, current_delivery_date ASC;
