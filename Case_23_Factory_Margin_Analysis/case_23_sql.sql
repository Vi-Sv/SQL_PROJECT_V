-- КЕЙС №23: ТРEХЭТАЖНЫЕ КAСКАДНЫЕ СТЕ-МАГИСТРАЛИ И АНАЛИЗ РЕНТАБЕЛЬНОСТИ ПРОИЗВОДСТВА
-- Бизнес-задача: Спроектировать многоуровневый отказоустойчивый конвейер вычисления маржинальности. Изолировать коммерческие позиции с отрицательной и нулевой рентабельностью (скрытые убытки) с одновременным отсечением тестовых логов холдинга.

WITH base_cleansing AS (
    -- ПЕРВЫЙ ЭТАП ОБРАБОТКИ (CTE 1): Стандартизация качества данных (Data Quality)
    SELECT 
        TRIM(LOWER(factory_name)) AS g1,  -- Нормализация текстового ввода: удаление пробелов и приведение к нижнему регистру
        COALESCE(production_cost, 0) AS g2, -- Валидация метрики себестоимости: замена NULL на числовой ноль
        COALESCE(market_price, 0) AS g3    -- Валидация метрики розничной цены: замена NULL на числовой ноль
    FROM product_manufacturing -- [Исправление] Исправлена опечатка моторики, дописана буква "c" в имя таблицы
),

margin_calc AS (
    -- ВТOРОЙ ЭТАП ОБРАБОТКИ (CTE 2): Каскадный расчет финансовой дельты
    -- [Исправление] Выровнен синтаксис объявления второго CTE-цеха в цепочке каскада
    SELECT 
        base_cleansing.g1 AS f1, 
        base_cleansing.g2 AS f2, 
        -- Чистая маржа = Розничная рыночная цена (g3) минус Производственная себестоимость (g2)
        (base_cleansing.g3 - base_cleansing.g2) AS f3 
    FROM base_materials -- Ссылка на результаты первого очищенного цеха
)

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование финальной аналитической витрины для директора завода
SELECT 
    margin_calc.f1 AS "Завод-изготовитель", -- [Исправление] Восстановлен текстовый маркер, добавлена буква "л"
    margin_calc.f3 AS "Маржа"              -- [Исправление] Вызывается корректное поле чистой маржи f3 (вместо себестоимости f2)
FROM margin_calc

-- ХИРУРГИЧЕСКОЕ СИТО WHERE: Жесткая многоуровневая фильтрация витрины
-- [Исправление] Пост-фильтрация убытков переориентирована на верный расчетный алиас маржи f3
WHERE margin_calc.f3 <= 0                
    AND margin_calc.f1 NOT LIKE '%тест%'   -- Исключение технического и отладочного мусора по маске строки

-- СОРТИРОВКА ВИТРИНЫ: Ранжирование позиций от минимального убытка к максимальному кассовому разрыву
ORDER BY margin_calc.f3 DESC;
