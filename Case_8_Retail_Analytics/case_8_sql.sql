-- КЕЙС №8: СОРТИРОВОЧНЫЙ ЧИТ-КОД NULLS LAST И ЗАЩИТА КАТАЛОГА В РИТЕЙЛЕ
-- Бизнес-задача: Сформировать консолидированный реестр продаж по всему каталогу товаров. Исключить риск дублирования строк (вспучивания сумм) и обеспечить корректный вывод нереализованных позиций с нулевым показателем.

SELECT 
    TRIM(LOWER(t1.item_name)) AS product_name, -- Нормализация текстового ввода: удаление концевых пробелов и приведение к нижнему регистру
    COALESCE(t2.total_quantity, 0) AS sales_volume -- Финальная валидация: нейтрализация внешних NULL от пустых связей LEFT JOIN

FROM products AS t1 -- Исходный справочник номенклатуры (Левая неприкасаемая таблица)

-- Левостороннее сопряжение со сжатым подзапросом гарантирует сохранение всех позиций каталога
LEFT JOIN (
    -- ПРЕДВАРИТЕЛЬНАЯ АГРЕГАЦИЯ: Схлопывание дублирующих строк продаж до этапа сшивания таблиц
    -- Это оптимизирует скорость работы сервера и блокирует риск логического размножения строк
    SELECT 
        item_id, 
        SUM(COALESCE(quantity, 0)) AS total_quantity -- Локальный предохранитель от пропусков NULL внутри лога
    FROM sales_logs
    GROUP BY item_id
) AS t2
ON t1.item_id = t2.item_id -- Фиксация ключей сопряжения данных

-- СОРТИРОВКА ВИТРИНЫ: Выводим наиболее продаваемые позиции наверх, а нулевые объемы автоматически смещаются в хвост отчета
ORDER BY sales_volume DESC;
