-- Бизнес-задача: Сформировать детализированный зарплатный реестр сотрудников с расчетом локального среднего оклада по каждому филиалу холдинга без агрегации и потери персональных строк.

SELECT
    emp_id AS employee_id,                               -- Уникальный номер сотрудника (Первичный ключ)
    TRIM(LOWER(branch_name)) AS clean_branch_name,       -- Нормализация географических наименований: удаление пробелов и приведение к нижнему регистру
    TRIM(LOWER(department)) AS clean_department,         -- Унификация наименований департаментов
    COALESCE(salary, 0) AS clean_salary,                 -- Валидация метрики: замена NULL на 0 для защиты математических вычислений
    
    -- ОКОННОЕ СЕКЦИОНИРОВАНИЕ: Расчет среднего оклада локально внутри каждого филиала (города).
    -- Конструкция PARTITION BY делит массив на независимые изолированные окна по городам,
    -- вычисляет среднее значение и дублирует его в каждую детальную строку без схлопывания отчета.
    AVG(COALESCE(salary, 0)) OVER (PARTITION BY TRIM(LOWER(branch_name)))::NUMERIC(10,2) AS branch_average_salary

FROM raw_salaries AS branch_separation

-- СОРТИРОВКА ВИТРИНЫ: Группировка вывода по алфавитному порядку нормализованных филиалов
ORDER BY clean_branch_name ASC;
