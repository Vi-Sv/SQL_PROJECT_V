-- КЕЙС №33: РЕФАКТОРИНГ ЛЕГАСИ-КОДА ЧЕРЕЗ CTE-МАГИСТРАЛИ И ПЛОСКИЙ PIVOT-АГРЕГАТ
-- Бизнес-задача: Оптимизировать критически неэффективный запрос, вызывающий зависание Highload-сервера. Заменить скалярные коррелированные подзапросы в блоке SELECT на отказоустойчивую модель предварительной условной агрегации за один физический проход СУБД.

WITH select_sum AS (
    -- ВЫЧИСЛИТЕЛЬНЫЙ ЦЕХ (CTE): Предварительное сжатие лога заказов и разворот категорий строк в Pivot-столбцы
    SELECT 
        users_id AS g1, -- Идентификатор пользователя (Ключ будущей сквозной сшивки один-к-одному)
        
        -- ДВОЙНОЙ РУБЕЖ ЗАЩИТЫ ДАННЫХ (DATA QUALITY): 
        -- Внутренний COALESCE зануляет NULL в чеках, а внешний COALESCE(..., 0) укрывает весь агрегат,
        -- гарантируя вывод константы 0 на витрину даже при полном отсутствии транзакций по категории.
        -- Строковые маски приведены к консистентному нижнему регистру 'it' и 'auto' под стать функции LOWER().
        COALESCE(SUM(CASE WHEN TRIM(LOWER(cat)) = 'it' THEN COALESCE(amount, 0) ELSE 0 END), 0) AS g2,
        COALESCE(SUM(CASE WHEN TRIM(LOWER(cat)) = 'auto' THEN COALESCE(amount, 0) ELSE 0 END), 0) AS g3 -- [Исправление] Сбриты избыточная запятая и зазор перед FROM
    FROM orders
    GROUP BY users_id -- Схлопывание дубликатов на нижнем уровне до сшивания миров
)

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Сопряжение справочника активных клиентов со стерильной CTE-матрицей
SELECT 
    t1.user_name AS "Имена_пользователей", 
    t2.g2 AS "IT",   -- [Исправление] Устранена коллизия адресации, вызов переведен на родительский алиас цеха t2
    t2.g3 AS "AUTO"  -- [Исправление] Устранена коллизия адресации, вызов переведен на родительский алиас цеха t2
FROM users AS t1

-- ЛЕВЫЙ МОСТ ДАННЫХ: Пришиваем сжатый цех. Левостороннее сопряжение намертво защищает "молчунов" от стирания
LEFT JOIN select_sum AS t2
    ON t1.users_id = t2.g1 -- [Исправление] Сшивка переведена на корректный внутренний алиас g1

-- ХИРУРГИЧЕСКОЕ СИТО WHERE: Выделяем только активный состав контрагентов холдинга
WHERE t1.status = 'active' -- [Исправление] Фильтр жестко привязан к объявленному префиксу таблицы t1

-- СОРТИРОВКА ВИТРИНЫ: Алфавитное упорядочивание финишного реестра по именам пользователей
ORDER BY t1.user_name ASC;
