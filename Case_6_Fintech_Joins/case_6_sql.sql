-- Бизнес-задача: Сформировать сквозной отчет по всей клиентской базе с расчетом совокупных затрат за отчетный период. Клиенты без транзакционной активности должны быть сохранены в реестре с нулевым балансом.

SELECT 
    us.user_id AS user_id,       -- Идентификатор пользователя (Первичный ключ справочника)
    us.phone AS user_phone,      -- Контактный номер телефона клиента
    
    -- Финальная валидация: заменяем пустые значения связей на 0 для фиксации клиентов без транзакций
    COALESCE(or2.total_payment, 0) AS total_payment_amount

FROM users AS us -- Исходный неприкасаемый справочник клиентов (Левая таблица)

-- Использование LEFT JOIN гарантирует сохранение всех строк из таблицы users
LEFT JOIN (
    -- ПРЕДВАРИТЕЛЬНАЯ АГРЕГАЦИЯ: Схлопывание дублирующих строк заказов до этапа сшивки таблиц
    -- Это предотвращает дублирование (вспучивание) строк и оптимизирует нагрузку на СУБД
    SELECT 
        user_id, 
        SUM(COALESCE(payment_amount, 0)) AS total_payment
    FROM orders
    GROUP BY user_id
) AS or2
ON us.user_id = or2.user_id; -- Фиксация ключей сопряжения данных
