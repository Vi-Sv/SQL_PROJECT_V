-- Бизнес-задача: Выполнить многомерный аналитический срез коммерческой эффективности торговых агентов. Реализовать ранжирование транзакций внутри продуктовых категорий и изолировать строго по одному абсолютному лидеру (Top-1) по объему прибыли на каждое направление.

SELECT
    t2.clean_agent AS "Агенты",
    t2.clean_category AS "Категория",
    t2.clean_profit AS "Прибыль",
    t2.row_rank AS "Топ_1"

FROM (
    -- ВНУТРЕННИЙ СЛОЙ («МАТРЕШКА»): Нормализация данных и развертывание аналитического нумератора
    SELECT 
        TRIM(LOWER(agent_name)) AS g1, 
        TRIM(LOWER(category)) AS g2, 
        COALESCE(profit, 0) AS g3,
        
        -- АНАЛИТИЧЕСКОЕ РАНЖИРОВАНИЕ: Функция ROW_NUMBER() присваивает уникальный порядковый номер каждой строке.
        -- PARTITION BY изолирует продажи внутри независимых расчетных окон по каждой категории.
        -- ORDER BY DESC выстраивает прибыль по убыванию, вынося абсолютного чемпиона наверх (ранг 1).
        ROW_NUMBER() OVER (
            PARTITION BY TRIM(LOWER(category)) 
            ORDER BY COALESCE(profit, 0) DESC
        ) AS g4
    FROM agent_sales
) AS t2

-- ХИРУРГИЧЕСКОЕ СИТО WHERE (КПП верхнего уровня):
-- Оставляем только строки со значением ранга 1. Весь последующий многотысячный хвост операционного шума отсекается.
-- Точный вызов родительского адреса t2.g4 блокирует риск неоднозначности полей в распределенных СУБД.
WHERE t2.g4 = 1 

-- СОРТИРОВКА ВИТРИНЫ: Регламентное упорядочивание по алфавитному признаку продуктовых категорий
ORDER BY 2 ASC;
