-- КЕЙС №26: КOРРEЛИРOВАННЫЕ ПОДЗАПРОСЫ И ЛOГИЧЕСКИЙ ДАТЧИК ИБ-АУДИТА
-- Бизнес-задача: Сформировать верифицированный («белый») список сотрудников департамента аналитики, не имеющих зафиксированных критических инцидентов информационной безопасности. Обеспечить максимальную производительность вычислительного контура за счет исключения избыточных сканирований.

WITH clean_staff AS (
    -- ВЫЧИСЛИТЕЛЬНЫЙ ЦЕХ (CTE): Предварительный отбор персонала и запуск датчика ИБ
    SELECT 
        TRIM(LOWER(emp_name)) AS g1 -- Нормализация текстового ввода: удаление концевых пробелов и приведение к нижнему регистру
    FROM staff_accounts AS t1 
    WHERE department = 'Аналитика' -- Первичный отсев по целевому структурному подразделению
    
      -- [Исправление] Убран избыточный дублирующий WHERE и скорректировано имя оператора NOT EXISTS
      AND NOT EXISTS ( 
          -- ЛОГИЧЕСКИЙ ДАТЧИК ПРЕРЫВАНИЯ: Сканирует таблицу инцидентов до первого совпадения
          SELECT 1 
          FROM security_incidents AS t2 
          -- Коррелированный мост: жесткая связь внутренних строк подзапроса с внешним справочником
          WHERE t2.emp_id = t1.emp_id 
            -- [Исправление] Ликвидирован дублирующий оператор AND
            AND t2.severity_level = 'CRITICAL' 
      ) -- Закрыта круглая скобка подзапроса EXISTS
) -- [Исправление] Дописана пропущенная внешняя скобка, закрывающая контур магистрали WITH

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Вывод очищенного реестра верифицированного персонала
SELECT 
    t2.g1 AS "Стерильные_аналитики" -- [Исправление орфографии] Исправлен символьный маркер колонки
FROM clean_staff AS t2

-- СОРТИРОВКА ВИТРИНЫ: Упорядочивание белого списка по алфавитному признаку
ORDER BY t2.g1 ASC;
