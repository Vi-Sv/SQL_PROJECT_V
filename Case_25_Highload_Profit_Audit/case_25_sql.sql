-- КЕЙС №25: ТРEХЭТАЖНЫЕ CTE-МАГИСТРАЛИ И СЕГМЕНТАЦИЯ VIP-ПРИБЫЛИ ХОЛДИНГА
-- Бизнес-задача: Спроектировать отказоустойчивый многоуровневый аналитический конвейер для вычисления чистой прибыли и выделения высокодоходного VIP-сегмента. Изолировать сделки с чистым профитом строго выше 500 000 единиц, исключив отладочный и тестовый мусор.

WITH raw_cleansing AS (
    -- ПЕРВЫЙ ЭТАП ОБРАБОТКИ (CTE 1): Стандартизация качества данных (Data Quality)
    SELECT 
        TRIM(LOWER(client_name)) AS g1,    -- [Исправление] Устранена опечатка сканера в имени цеха. Очистка строкового регистра и пробелов
        COALESCE(gross_revenue, 0) AS g2, -- Валидация метрики валовой выручки: замена NULL на числовой ноль
        COALESCE(tax_deduction, 0) AS g3   -- Валидация метрики налоговых вычетов: замена NULL на числовой ноль
    FROM sales_deals
),

profit_calculation AS (
    -- ВТOРОЙ ЭТАП ОБРАБОТКИ (CTE 2): Каскадный расчет финансовой дельты
    -- [Исправление] Полностью ликвидировано месиво распознавания сканера. Контур замкнут на чистые алиасы первого этажа
    SELECT 
        g1 AS f1, 
        -- Чистая прибыль = Валовая выручка (g2) минус Налоговые вычеты (g3)
        (g2 - g3) AS f2 
    FROM raw_cleansing -- Последовательный вызов данных из предыдущей магистрали
)

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование финальной аналитической витрины для инвесторов
SELECT 
    profit_calculation.f1 AS "Имя_клиента",   -- [Исправление] Коррекция орфографии в алиасе вывода
    profit_calculation.f2 AS "Чистая_прибыль"
FROM profit_calculation -- Вызов готового откомпилированного массива данных

-- ХИРУРГИЧЕСКОЕ СИТО WHERE: Жесткая многоуровневая пост-фильтрация
WHERE profit_calculation.f2 > 500000 -- Фильтрация по установленному VIP-порогу строгого превосходства
    -- [Исправление] Восстановлен утерянный сканером маркер отсечения технических аккаунтов
    AND profit_calculation.f1 NOT LIKE '%тест%' 

-- СОРТИРОВКА ВИТРИНЫ: Ранжирование VIP-сделок от максимального профита к минимальному лимиту
ORDER BY profit_calculation.f2 DESC;
