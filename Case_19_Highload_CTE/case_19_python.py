# 2. ИЗОЛИРОВАННЫЙ ЦЕХ ОЧИСТКИ ДАННЫХ (Аналог конструкции WITH clean_data AS)
# Создаем копию датафрейма для проведения процедур стандартизации качества данных (Data Quality)
clean_df = df.copy()

# Нормализуем текстовые наименования: сбриваем концевые пробелы и приводим к нижнему регистру
clean_df['agent_name'] = clean_df['agent_name'].astype(str).str.strip().str.lower()

# Валидируем финансовую метрику: нейтрализуем пропуски NULL нулями (.fillna(0)) 
# и переводим возвраты в абсолютные положительные значения (.abs())
clean_df['amount'] = clean_df['amount'].fillna(0).abs().astype(int)

# 3. ГЛАВНЫЙ КОНТУР ФИЛЬТРАЦИИ И ВЫВОД НА ВИТРИНУ
# Применяем сито строгого неравенства для локализации аномальных VIP-сделок (Аналог WHERE > 300000)
final_view = clean_df[clean_df['amount'] > 300000].copy()

# Сортируем итоговый результат по убыванию финансового веса сумм (Аналог ORDER BY DESC)
final_view = final_view.sort_values(by='amount', ascending=False)

# Выделяем целевые аналитические колонки отчета для руководства холдинга
final_view = final_view[['agent_name', 'amount']]
