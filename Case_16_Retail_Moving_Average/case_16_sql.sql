-- Бизнес-задача: Реализовать алгоритм сглаживания тренда выручки торговых точек. Рассчитать трехдневное скользящее среднее значение операционного дохода на основе календарных интервалов времени без потери детализации исходных торговых дней.

SELECT
    shop_id AS shop_id,                    -- Уникальный номер торговой точки (филиала)
    report_date AS report_timestamp,        -- Дата и время фиксации торгового дня
    COALESCE(daily_revenue, 0) AS clean_revenue, -- Валидация метрики: замена NULL на 0 для защиты математического движка СУБД
    
    -- СКОЛЬЗЯЩЕЕ ОКOННОЕ СРЕДНЕЕ: Конструкция PARTITION BY изолирует расчетные контуры по магазинам.
    -- ORDER BY упорядочивает транзитный массив по шкале времени.
    -- Конструкция RANGE BETWEEN INTERVAL '2' DAY PRECEDING заставляет движок СУБД вычислять 
    -- среднее значение строго по скользящему календарному окну за последние 72 часа (2 дня назад + текущий день),
    -- полностью нивелируя проблему пропущенных торговых дней и дублирующихся строк внутри одних суток.
    AVG(COALESCE(daily_revenue, 0)) OVER (
        PARTITION BY shop_id 
        ORDER BY report_date 
        RANGE BETWEEN INTERVAL '2' DAY PRECEDING AND CURRENT ROW
    ) AS rolling_average_3d -- Коррекция: Интегрировано утерянное имя расчетного алиаса

FROM daily_shop_revenue

-- СОРТИРОВКА ВИТРИНЫ: Упорядочивание вывода по идентификаторам магазинов и хронологическому ходу времени
ORDER BY 1, 2 ASC;
