-- КЕЙС №27: МНОЖЕСТВЕННЫЕ ПОДЗАПРОСЫ И СИТО ПРЕДИКАТОВ IN В РИТЕЙЛ-МАРКЕТИНГЕ
-- Бизнес-задача: Сформировать детализированный реестр доходов филиалов. Выделить полную структуру финансовых показателей по тем городам, которые прошли предварительный отбор по южной зоне холдинга и преодолели лимит прибыли в 250 000 единиц.

WITH top_profit_subquery AS (
    -- ВЫЧИСЛИТЕЛЬНЫЙ ЦЕХ (CTE): Предварительный отбор уникальных целевых локаций
    SELECT DISTINCT 
        TRIM(LOWER(city_name)) AS g1 -- [Исправление] Удалена избыточная запятая перед FROM. Очистка строкового регистра и пробелов
    FROM branch_revenues
    WHERE COALESCE(monthly_profit, 0) > 250000 
      AND TRIM(LOWER(region_zone)) = 'юг'
) -- [Исправление] Восстановлена утерянная скобка, закрывающая контур СТЕ-магистрали

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование витрины показателей по городам-лидерам
SELECT 
    TRIM(LOWER(city_name)) AS "Зачищенный_город",
    COALESCE(monthly_profit, 0) AS "Прибыль"
FROM branch_revenues

-- СИТО ГОНЦА (Предикативная фильтрация): Динамическая сверка текущей строки со справочником из CTE
WHERE TRIM(LOWER(city_name)) IN (SELECT g1 FROM top_profit_subquery) 

-- СОРТИРОВКА ВИТРИНЫ: Ранжирование вывода по убыванию объема прибыли (вторая колонка селекта)
ORDER BY 2 DESC;
