-- КЕЙС №30: МНОГОСТУПЕНЧАТЫЕ РЕКУРСИВНЫЕ CTE И ПОСТРОЕНИЕ ГРАФОВЫХ ИЕРАРХИЙ ШТАТА
-- СТАТУС: ТЕХНИЧЕСКАЯ ДОКУМЕНТАЦИЯ ПОВЫШЕННОЙ СЛОЖНОСТИ (R&D-ПOЛИГOН) - наличие данного кейса исключительно в целях сохранения данного инструмента и понимания его механизма
-- Бизнес-задача: Развернуть динамическое дерево корпоративной подчиненности холдинга. Вычислить абсолютный уровень вложенности (иерархический уровень) для каждого сотрудника, начиная от топ-менеджмента и спускаясь к линейному персоналу.

WITH RECURSIVE hierarchy_tree AS ( -- [Исправление] Интегрирован утерянный токен AS, открыта внешняя скобка каскада
    
    -- АНКЕРНЫЙ ЧЛЕН (БАЗА РЕКУРСИИ): Точка входа. Локализация топ-менеджмента (Генеральный директор), у которого нет вышестоящих начальников
    SELECT 
        emp_id AS g1, 
        TRIM(LOWER(emp_name)) AS g2, 
        manager_id AS g3, 
        1 AS level -- Стартовая точка отсчета глубины графа
    FROM company_structure
    WHERE manager_id IS NULL -- Триггер верхнего узла дерева
    
    UNION ALL
    
    -- РЕКУРСИВНЫЙ ЧЛЕН (ШАГ РЕКУРСИИ): Спуск вниз по веткам зависимостей
    -- Вычислительный цех последовательно вызывает сам себя (hierarchy_tree AS t1), сопрягая подчиненных с шефами
    SELECT 
        t2.emp_id, 
        TRIM(LOWER(t2.emp_name)), -- [Исправление] Префикс таблицы t2 корректно перенесен внутрь скалярных функций
        t2.manager_id, 
        t1.level + 1 -- Инкремент глубины залегания узла (+1 шаг вниз по иерархии)
    FROM company_structure AS t2
    
    -- Рекурсивный мост связи: сшиваем внешнюю грязную таблицу с внутренними результатами предыдущего шага рекурсии
    INNER JOIN hierarchy_tree AS t1 
        ON t2.manager_id = t1.g1 -- [Исправление] Сшивка переориентирована на строгий выходной алиас g1
)

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование финальной древовидной витрины организационной структуры
SELECT 
    g1 AS "ID_Сотрудника", 
    g2 AS "Имя", 
    g3 AS "ID_Начальника", 
    level AS "Уровень_Иерархии" 
FROM hierarchy_tree

-- СОРТИРОВКА ВИТРИНЫ: Последовательный вывод от высшего руководства вниз к линейным позициям
ORDER BY level ASC; -- [Исправление] Устранена опечатка моторики в операторе сортировки
